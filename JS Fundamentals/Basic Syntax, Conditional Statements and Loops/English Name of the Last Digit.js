function lastDigit(digit){

    let digitName = digit % 10
    
    if(digitName === 0){
        console.log('zero')
    }else if(digitName === 1){
        console.log('one')
    }else if(digitName === 2){
        console.log('two')
    }else if(digitName === 3){
        console.log('three')
    }else if(digitName === 4){
        console.log('four')
    }else if(digitName === 5){
        console.log('five')
    }else if(digitName === 6){
        console.log('six')
    }else if(digitName === 7){
        console.log('seven')
    }else if(digitName === 8){
        console.log('eight')
    }else if(digitName === 9){
        console.log('nine')
    }

}
